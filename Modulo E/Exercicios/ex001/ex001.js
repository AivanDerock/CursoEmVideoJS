console.log("Essa é a forma de sequencia:");
console.log("Hello, World!");
console.log("Hello, World!");
console.log("Hello, World!");

console.log("Essa é a forma com While:");
let c = 1
while (c <= 3) {
  console.log("Hello, World!");
  c++ // c = c + 1
}

let d = 1
while (d <= 10) {
  console.log(`Passo ${d}`);
  d++ // d = d + 1
}

console.log("Essa é a forma com Do While:");
let a = 1
do {
  console.log(`Passo ${a}`);
  a++ // a = a + 1
} while (a <= 3);